public class TilesLinkItemsBean {

	private String headtext;
	private String tilesimage;
	private String linktext;
	private String linkurl;
	private String arrowvariation;

	public String getHeadtext() {
		return headtext;
	}

	public void setHeadtext(String headtext) {
		this.headtext = headtext;
	}

	public String getTilesimage() {
		return tilesimage;
	}

	public void setTilesimage(String tilesimage) {
		this.tilesimage = tilesimage;
	}

	public String getLinktext() {
		return linktext;
	}

	public void setLinktext(String linktext) {
		this.linktext = linktext;
	}

	public String getLinkurl() {
		return linkurl;
	}

	public void setLinkurl(String linkurl) {
		this.linkurl = linkurl;
	}

	public String getArrowvariation() {
		return arrowvariation;
	}

	public void setArrowvariation(String arrowvariation) {
		this.arrowvariation = arrowvariation;
	}



}
